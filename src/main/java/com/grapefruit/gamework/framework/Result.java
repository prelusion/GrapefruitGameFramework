package com.grapefruit.gamework.framework;

/**
 *  A result of a game.
 */
public enum Result {
    /**
     *  The game has a winner
     */
    WINNER,
    /**
     *  The game is a tie.
     */
    TIE,
    /**
     *  The game does not have a winner or tie.
     */
    NONE
}
